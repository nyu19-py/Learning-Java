package com.Learnjava;

public class literals {
    public static void main(String[] args) {
        int a = 17;
        short b = 222;
        byte c = 126;
        float value = 10.44f;
        double dValue = 1110.6545;
        long lValue = 134838L;
        String ch = "Hello";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(ch);
    }
}
